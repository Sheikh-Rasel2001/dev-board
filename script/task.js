document.getElementById('completed-btn').addEventListener('click', function(){
// task assign
const task = document.getElementById('task').innerText
const convertTask = parseInt(task);
if(convertTask >= task){
    const sub = convertTask - 1;
    document.getElementById('task').innerText = sub;
}
// task assign
const addTask = document.getElementById('add-task').innerText;
const convertedAddTask = parseInt(addTask);
if(convertedAddTask >= addTask){
    const sum = convertedAddTask + 1;
    document.getElementById('add-task').innerText = sum;
    
}
})
